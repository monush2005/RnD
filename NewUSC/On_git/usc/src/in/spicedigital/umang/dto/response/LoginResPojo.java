package in.spicedigital.umang.dto.response;

public class LoginResPojo {

	// response_status
	private String rs;
	// response_code
	private String rc;
	// response_description
	private String rd;

	private LoginResPojo.Pd pd;

	public LoginResPojo.Pd getPd() {
		return pd;
	}

	public void setPd(LoginResPojo.Pd pd) {
		this.pd = pd;
	}

	public static class Pd {

		private String flagexpiry;
		private String flagfirst;
		private String email;
		private String mno;
		

		@Override
		public String toString() {
			return "Pd [flagexpiry=" + flagexpiry + ", flagfirst=" + flagfirst + ", email=" + email + ", mno=" + mno + "]";
		}

		public String getEmail() {
			return email;
		}

		public void setEmail(String email) {
			this.email = email;
		}

		public String getMno() {
			return mno;
		}

		public void setMno(String mno) {
			this.mno = mno;
		}

		public String getFlagexpiry() {
			return flagexpiry;
		}

		public void setFlagexpiry(String flagexpiry) {
			this.flagexpiry = flagexpiry;
		}

		public String getFlagfirst() {
			return flagfirst;
		}

		public void setFlagfirst(String flagfirst) {
			this.flagfirst = flagfirst;
		}

	}

	public String getRs() {
		return rs;
	}

	public void setRs(String rs) {
		this.rs = rs;
	}

	public String getRc() {
		return rc;
	}

	public void setRc(String rc) {
		this.rc = rc;
	}

	public String getRd() {
		return rd;
	}

	public void setRd(String rd) {
		this.rd = rd;
	}

	@Override
	public String toString() {
		return "LoginResPojo [rs=" + rs + ", rc=" + rc + ", rd=" + rd + ", pd=" + pd + "]";
	}

}
