package in.spicedigital.umang.usc.request;

public class BroadcastRequestPojo {
	private String userId;
	private String appId;
	private String cgId;
	private String msg;
	private boolean subUsers;
	private String trkr;
	private String lang;
	private String token;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getAppId() {
		return appId;
	}
	public void setAppId(String appId) {
		this.appId = appId;
	}
	public String getCgId() {
		return cgId;
	}
	public void setCgId(String cgId) {
		this.cgId = cgId;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public boolean getSubUsers() {
		return subUsers;
	}
	public void setSubUsers(boolean subUsers) {
		this.subUsers = subUsers;
	}
	public String getTrkr() {
		return trkr;
	}
	public void setTrkr(String trkr) {
		this.trkr = trkr;
	}
	public String getLang() {
		return lang;
	}
	public void setLang(String lang) {
		this.lang = lang;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	@Override
	public String toString() {
		return "BroadcastRequestPojo [userId=" + userId + ", appId=" + appId + ", cgId=" + cgId + ", msg=" + msg
				+ ", subUsers=" + subUsers + ", trkr=" + trkr + ", lang=" + lang + ", token=" + token + ", getUserId()="
				+ getUserId() + ", getAppId()=" + getAppId() + ", getCgId()=" + getCgId() + ", getMsg()=" + getMsg()
				+ ", isSubUsers()=" + getSubUsers() + ", getTrkr()=" + getTrkr() + ", getLang()=" + getLang()
				+ ", getToken()=" + getToken() + ", getClass()=" + getClass() + ", hashCode()=" + hashCode()
				+ ", toString()=" + super.toString() + "]";
	}
}
